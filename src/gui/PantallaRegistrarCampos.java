    private void jButtonAgregrLoteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAgregrLoteActionPerformed
        
        
        if (this.establecimiento.agregarLote(this.lote, Integer.parseInt(txtNumeroLote.getText()),Integer.parseInt(txtSuperficieLote.getText()) ) == false) 
        {
            LabelErrorNroLote.setVisible(true);   
            LabelErrorSuperficieLote.setVisible(true);   

        }
        else
        {
            LabelErrorNroLote.setVisible(false); 
            LabelErrorSuperficieLote.setVisible(false);   
            
            Lote ultimoLote =this.establecimiento.lotes().get(establecimiento.lotes().size() - 1);           
            ultimoLote.superficie(this.superficieLote);
        
            Suelo unSueloSeleccionado= (Suelo) ComboBoxTipoSuelo.getSelectedItem();
            ultimoLote.suelo(unSueloSeleccionado);
                 
            this.superficieLote = Integer.parseInt(txtSuperficieLote.getText());
            ultimoLote.superficie(this.superficieLote);
         
            tablaLotesModel.setLotes(this.establecimiento.lotes());
            tablaLotesModel.fireTableDataChanged();

        }            
       
    }//GEN-LAST:event_jButtonAgregrLoteActionPerformed

