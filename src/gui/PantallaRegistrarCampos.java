    // controla la existencia de un Establecimiento con el mismo nombre imgresado.
    private void txtNombreEstablecimientoFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtNombreEstablecimientoFocusLost

        if ( ! txtNombreEstablecimiento.getText().isEmpty())
        {

            this.establecimiento.nombre(txtNombreEstablecimiento.getText());

            if ( this.empresaAdministradora.agregarEstablecimiento(establecimiento) == false)
            {
                LabelErrorNombre.setVisible(true);

            }
            else
            {
                txtSuperficie.setEditable(true);
                LabelErrorNombre.setVisible(false);
            }
        } else

        LabelErrorNombre.setVisible(true);

    }//GEN-LAST:event_txtNombreEstablecimientoFocusLost

    private void txtSuperficieFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtSuperficieFocusLost

        if ( txtSuperficie.getText().isEmpty() == false ){
            this.superficieEstablecimiento = Integer.parseInt(txtSuperficie.getText());
            if ( this.superficieEstablecimiento < 1 )
            LabelErrorSuperficie.setVisible(true);
            else
            {
                this.establecimiento.superficieEstablecimiento(this.superficieEstablecimiento);
                txtNumeroLote.setEditable(true);
                LabelErrorSuperficie.setVisible(false);
                txtSuperficie.setEditable(false);
                txtNombreEstablecimiento.setEditable(false);
            }
        }
        else
        LabelErrorSuperficie.setVisible(true);

    }//GEN-LAST:event_txtSuperficieFocusLost

